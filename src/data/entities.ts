/** Reverse mapping: HTML entity string to character, e.g. `"&check;" -> "\u2713"` */
export const HTML_ENTITY_TO_CHAR: ReadonlyMap<string, string> = new Map<
  string,
  string
>(
  Array.from(HTML_ENTITIES.entries()).map(([cp, entity]) => [
    entity,
    String.fromCodePoint(cp),
  ]),
);
